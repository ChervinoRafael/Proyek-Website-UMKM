        <h1>Apa Fungsi dari Tisu Toilet?</h1>
        <p> Sebelum membahas mengenai fungsinya, kamu perlu kenali apa itu tisu toilet. Tisu toilet merupakan kertas yang dimanfaatkan untuk membersihkan alat kelamin dan anus dari kotoran sesudah proses buang air kecil dan besar. Kebanyakan tisu toilet diciptakan agar mudah terurai sehingga bekas pemakaian tisu toilet sekali pakai ini nggak bakal menyumbat saluran limbah.
            Umumnya, tisu toilet ini berbentuk potongan kertas yang digulung dan memiliki lubang pada bagian tengahnya agar bisa disimpan di dalam dispenser dekat toilet. Awal pertama kali ditemukan, tisu toilet ini diproduksi untuk keperluan sanitasi. Selain di Indonesia, tisu toilet ini juga mudah kamu temukan dan digunakan oleh orang budaya Barat, seperti Prancis dan Amerika.

        </p>

        <h2>Macam-Macam Ukuran Tisu Toilet</h2>
        <p> Dari informasi di atas, kamu juga perlu tahu ternyata tisu toilet dibuat dengan ukuran yang berbeda-beda. Sebelumnya dijelaskan juga, ukuran tisu toilet ini bisa berukuran sekitar 2 kali hingga 3 kali kaki atau 60 x 90 cm pada masa Dinasting Ming. Tapi, seiring berjalannya waktu tisu toilet memiliki ukuran yang berbeda, yaitu 9 x 20 cm dan ada juga 10 x 10 cm. Tergantung pada penggunaannya.



            Selain pada ukuran, lapisan pada tisu toilet ini juga berbeda dan biasanya terdiri dari tiga macam lapisan. Ada 1 ply untuk kamu yang ingin penggunaan lebih hemat, 2 ply untuk kamu yang mengutamakan kenyamanan di kulit, dan 3 ply untuk mengutamakan ketebalan dan kekuatan tisu. Tapi, untuk kekuatan tisu toilet yang nggak mudah sobek, sebaiknya kamu memilih lapisan 3 ply, ya.



            Bukan cuma itu, tisu toilet dengan lapisan 3 ply ini juga memiliki daya serap lebih baik jika dibandingkan dengan 1 ply. Sementara dari segi harga, tisu 1 ply dan 2 ply memang dijual lebih murah daripada tisu toilet 3 ply. Terlebih lagi, tekstur pada tisu toilet 2 ply dan 3 ply ini juga lembut di kulit dan cocok untuk kamu yang memiliki kulit sensitif.</p>

        <h2>Cara Menggunakan Tisu Toilet dengan Efisien</h2>
        <p> Sesuai dengan fungsinya, tisu toilet ini memiliki kegunaan yang begitu penting untuk daerah intim. Maka dari itu, penggunaan tisu toilet pun nggak boleh dilakukan dengan sembarang. Kamu perlu ketahui beberapa cara dan anjuran untuk menggunakan tisu toilet dengan efisien di bawah ini.</p>

        <h3>Pilih Tisu Tanpa Kandungan Parfum</h3>
        <p> Cara pertama adalah dengan memiliki tisu toilet tanpa kandungan parfum sama sekali. Mengingat tisu toilet ini dirancang menjadi bahan lembut, mudah terurai, dan nggak mengandung bahan pemutih maupun pewangi. Disamping itu, adanya bahan kimia yang dibuat pada tisu toilet ini bisa mengganggu pH normal pada area intim.Hal itu dapat menimbulkan resiko pertumbuhan bakteri yang mengakibatkan iritasi atau infeksi. Oleh karena itu, agar tisu toilet kamu tetap higienis dan bersih, maka ada baiknya untuk menyimpan di dispenser tissue roll.</p>

        <h3>Basuh dengan Arah yang Benar</h3>
        <p> Ketika membasuh area intim dengan menggunakan tisu toilet, kamu pun sebaiknya dilakukan dari arah yang benar, yaitu mulai dari arah depan ke belakang. Mengingat jika kamu membasuhnya dari arah belakang ke depan, bisa aja membuat kotoran berpindah dari area uretra ke depan, sehingga menyebabkan infeksi.</p>

        <h3>Menggosok dengan Lembut</h3>
        <p> Cara selanjutnya adalah dengan menggosokkan tisu toilet ke area intim secara lembut tanpa adanya tekanan tinggi. Hal itu pun juga dikatakan oleh Dr. Joel Krachman, MD, Kepala Gastroenterologi Atlanticare Regional Medical Center.



            Beliau menjelaskan ketika mengusap tisu ke area dubur, ada baiknya cukup dilakukan secara perlahan dan nggak perlu menggosok dengan cepat. Hal itu dilakukan agar kulit disekitarnya nggak iritasi dan infeksi.</p>

