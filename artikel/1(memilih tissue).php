        <h5>Bahan Kimia yang Terkandung dalam Tisu</h5>
        <p class="article-content">
            Ada berbagai jenis tisu yang tersedia di pasaran saat ini. Sebelum membeli, perhatikan bahan tisu yang tercantum pada kemasan. Kebanyakan produsen akan mencantumkan bahwa tisu dibuat dari 100% serat alami. Ini maksudnya produk tersebut dibuat dari serat atau pulp kayu baru, bukan hasil daur ulang. Berikut adalah bahan-bahan kimia berbahaya yang mungkin terkandung dalam produk tisu, entah itu tisu wajah, tisu makan, atau tisu toilet yang biasa Anda beli.
        </p>

        <h6>1.1 Pemutih Klorin</h6>
        <p class="article-content">
            Banyak tisu yang dijual mengandung pemutih dari bahan kimia klorin dan turunannya. Proses pewarnaan tisu ini akan melepas zat dioksin dan furan. Keduanya adalah zat yang bersifat karsinogenik (menyebabkan kanker) serta mutagenik (menyebabkan mutasi DNA). Menurut Badan Kesehatan Dunia (WHO), dioksin merupakan zat beracun yang berbahaya bagi kesehatan. Jika manusia terkontaminasi zat ini, dalam jangka pendeknya Anda berisiko mengalami lesi kulit dan gangguan fungsi hati.
        </p>

        <h6>1.2 Logam Berat</h6>
        <p class="article-content">
            Hati-hati jika Anda membeli tisu yang terbuat dari bahan-bahan daur ulang kertas atau karton bekas. Bahan bekas yang dipakai untuk mengolah tisu biasanya mengandung berbagai jenis logam berat seperti timbal, merkuri, kadmium, dan arsenik. Terkontaminasi zat-zat logam berat tersebut berisiko menyebabkan kerusakan pada otak dan sistem saraf manusia.
        </p>

        <h6>1.3 Bisphenol-A (BPA)</h6>
        <p class="article-content">
            Pada jenis tisu daur ulang, kerap ditemukan kandungan bisphenol-A (BPA) yang berpotensi menyebabkan berbagai masalah kesehatan. BPA terdapat pada jenis kertas tertentu yang digunakan saat mencetak bon atau tiket bioskop. Tinta yang digunakan untuk mencetak koran atau majalah juga mengandung BPA.
        </p>

        <h5>Dampak Limbah Tisu bagi Lingkungan</h5>
        <p class="article-content">
            Bahan-bahan kimia yang telah disebutkan di atas tidak akan hancur atau terurai setelah Anda menggunakan dan membuang tisu tersebut. Pada banyak kasus, tisu dengan kandungan racun yang tinggi akan dibuang sebagai limbah dan mencemari lingkungan.
        </p>

